    elseif strcmp(type,'supported')
        A1 = flex2d.getnearquad(npatches,norders,ixyzs, ...
          iptype,npts,srccoefs,srcvals,targinfo,ipatch_id, uvs_targ, eps,...
            iquadtype,nnz, ...
          row_ptr,col_ind,iquad,rfac0,dpars,zpars,nquad,'dir');
        A2 = flex2d.getnearquad(npatches,norders,ixyzs, ...
          iptype,npts,srccoefs,srcvals,targinfo,ipatch_id, uvs_targ, eps,...
            iquadtype,nnz, ...
          row_ptr,col_ind,iquad,rfac0,dpars,zpars,nquad,'supp2');
        A = [A1,A2];
    elseif strcmp(type,'clamped')
        A1 = flex2d.getnearquad(npatches,norders,ixyzs, ...
          iptype,npts,srccoefs,srcvals,targinfo,ipatch_id, uvs_targ, eps,...
            iquadtype,nnz, ...
          row_ptr,col_ind,iquad,rfac0,dpars,zpars,nquad,'dir');
        A2 = flex2d.getnearquad(npatches,norders,ixyzs, ...
          iptype,npts,srccoefs,srcvals,targinfo,ipatch_id, uvs_targ, eps,...
            iquadtype,nnz, ...
          row_ptr,col_ind,iquad,rfac0,dpars,zpars,nquad,'neu');
        A = [A1,A2];
    end

end
